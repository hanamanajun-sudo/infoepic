for (const [filename, addition] of Object.entries(specialAdditions)) {
  const filepath = `${blogDir}/${filename}`;
  const base = specialBase[filename] || BASE;
  try {
    const original = execSync(`git show ${base}:"${filepath}"`, { encoding: 'utf-8' });
    const newContent = original.trimEnd() + '\n\n' + addition.trimStart() + '\n';
    writeFileSync(filepath, newContent, 'utf-8');
    const stripped = newContent.replace(/\s/g, '').length;
    console.log(`${stripped >= 1500 ? 'OK' : 'NG'} ${stripped}\t${filename.substring(0, 50)} [special]`);
    ok++;
  } catch (e) {
    console.error(`FAIL\t${filename}\n  ${e.message.split('\n')[0]}`);
    fail++;
  }
}

console.log(`\n완료: ${ok}개 복원, ${fail}개 실패`);
